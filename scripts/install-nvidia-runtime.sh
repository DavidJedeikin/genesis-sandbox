#!/bin/bash

curl -fsSL https://nvidia.github.io/libnvidia-container/gpgkey | gpg --dearmor -o /usr/share/keyrings/nvidia-container-toolkit-keyring.gpg
curl -s -L https://nvidia.github.io/libnvidia-container/stable/deb/nvidia-container-toolkit.list | \
  sed 's#deb https://#deb [signed-by=/usr/share/keyrings/nvidia-container-toolkit-keyring.gpg] https://#g' > /etc/apt/sources.list.d/nvidia-docker.list
apt update && apt -y install nvidia-container-toolkit
nvidia-ctk runtime configure --runtime=docker # command will update /etc/docker/daemon.json with nvidia runtime.
systemctl restart docker 
